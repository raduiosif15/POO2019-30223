
public class Medicament {
	private double pret;
	private String numeFarmacie;
	private String denumireMedicament;

	public double getPret() {
		return pret;
	}
	public void setPret(double pret) {
		this.pret = pret;
	}
	public String getNumeFarmacie() {
		return numeFarmacie;
	}
	public void setNumeFarmacie(String numeFarmacie) {
		this.numeFarmacie = numeFarmacie;
	}
	public String getDenumireMedicament() {
		return denumireMedicament;
	}
	public void setDenumireMedicament(String denumireMedicament) {
		this.denumireMedicament = denumireMedicament;
	}

	
	
}
