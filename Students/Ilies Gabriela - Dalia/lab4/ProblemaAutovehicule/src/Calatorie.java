
public class Calatorie {
	private String destinatie;
	private String data_plecare;
	private String data_sosire;
	private Sofer sofer;
	private Autovehicul autovechicul;
	public String getDestinatie() {
		return destinatie;
	}
	public void setDestinatie(String destinatie) {
		this.destinatie = destinatie;
	}
	public String getData_plecare() {
		return data_plecare;
	}
	public void setData_plecare(String data_plecare) {
		this.data_plecare = data_plecare;
	}
	public String getData_sosire() {
		return data_sosire;
	}
	public void setData_sosire(String data_sosire) {
		this.data_sosire = data_sosire;
	}
	
	public Sofer getSofer() {
		return sofer;
	}
	public void setSofer(Sofer sofer) {
		this.sofer = sofer;
	}
	public Autovehicul getAutovechicul() {
		return autovechicul;
	}
	public void setAutovechicul(Autovehicul autovechicul) {
		this.autovechicul = autovechicul;
	}
	
}
