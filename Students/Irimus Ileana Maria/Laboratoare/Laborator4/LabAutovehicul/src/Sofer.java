
public class Sofer {

	private String nume;
	private String prenume;
	private double varsta;
	private int numarPermisConducere;
	
	public String getNume() {
		return this.nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public String getPrenume() {
		return this.prenume;
	}
	public void setPrenume(String prenume) {
		this.prenume = prenume;
	}
	public double getVarsta() {
		return this.varsta;
	}
	public void setVarsta(int varsta) {
		this.varsta = varsta;
	}
	public int getNumarPermisConducere() {
		return this.numarPermisConducere;
	}
	public void setNumarPermisConducere(int numarPermisConducere) {
		this.numarPermisConducere = numarPermisConducere;
	}

}
