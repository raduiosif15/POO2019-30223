public class Muzica {
	public int nrCD;
	public String genMuzical;
	public int nrMelodii;
	public int durataMinute;
	public int durataSecunde;

	public Muzica(int nrCD, String genMuzical) {
		this.nrCD = nrCD;
		this.genMuzical = genMuzical;
	}

	public int getNrCD() {
		return this.nrCD;
	}

	public int getGenMuzical() {
		return this.getGenMuzical();
	}

}
