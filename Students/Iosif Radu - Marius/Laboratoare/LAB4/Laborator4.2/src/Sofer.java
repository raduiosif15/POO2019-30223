public class Sofer {
	public String nume;
	public String prenume;
	public int varsta;
	public String nrPermis;
	public char categorieMaxima;

	public Sofer(String nume, String prenume, int varsta, String nrPermis, char categorieMaxima) {
		this.nume = nume;
		this.prenume = prenume;
		this.varsta = varsta;
		this.nrPermis = nrPermis;
		this.categorieMaxima = categorieMaxima;
	}
}
