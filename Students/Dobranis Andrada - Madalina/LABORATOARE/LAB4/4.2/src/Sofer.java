
public class Sofer {
	private String nume;
	private String prenume;
	private int varsta;
	private int nrPermisConducere;
	
	public void setNume(String newName) { this.nume = newName; }
	public void setPrenume(String newPrenume) { this.prenume = newPrenume; }
	public void setVarsta(int newVarsta) { this.varsta = newVarsta; }
	public void setNrPermisConducere(int newNrPermisConducere) { this.nrPermisConducere = newNrPermisConducere; }
	
	public String getNume() { return nume; }
	public String getPrenume() { return prenume; }
	public int getVarsta() { return varsta; }
	public int getNrPermisConducere() { return nrPermisConducere; }



}
